// Helper functions
export const getProductsByCategory = (category: string): Product[] => {
  return products.filter((product) => product.category === category)
}

export const getFeaturedProducts = (): Product[] => {
  return products.filter((product) => product.featured)
}

export const searchProducts = (query: string): Product[] => {
  const normalize = (s: string | undefined): string =>
    (s || "")
      .toLowerCase()
      .normalize("NFKC")
      .replace(/\u0640/g, "") // remove tatweel
      .replace(/\s+/g, " ")
      .trim()

  const q = normalize(query)
  if (!q) return products

  return products.filter((product) => {
    const fields = [
      product.name,
      product.description,
      product.brand,
      product.category,
      ...(product.tags || []),
    ]
    const searchableText = normalize(fields.join(" "))
    return searchableText.includes(q)
  })
}

export const sortProducts = (products: Product[], sortBy: string): Product[] => {
  const sorted = [...products]

  switch (sortBy) {
    case "name-asc":
      return sorted.sort((a, b) => a.name.localeCompare(b.name))
    case "name-desc":
      return sorted.sort((a, b) => b.name.localeCompare(a.name))
    case "price-asc":
      return sorted.sort((a, b) => a.price - b.price)
    case "price-desc":
      return sorted.sort((a, b) => b.price - a.price)
    case "featured":
      return sorted.sort((a, b) => {
        if (a.featured && !b.featured) return -1
        if (!a.featured && b.featured) return 1
        return 0
      })
    default:
      return sorted
  }
}

export const filterProducts = (products: Product[], filters: FilterOptions): Product[] => {
  return products.filter((product) => {
    // Category filter
    if (filters.categories.length > 0 && !filters.categories.includes(product.category)) {
      return false
    }

    // Brand filter
    if (filters.brands.length > 0 && !filters.brands.includes(product.brand)) {
      return false
    }

    // Color filter
    if (filters.colors.length > 0 && !filters.colors.some((color) => product.colors.includes(color))) {
      return false
    }

    // Size filter
    if (filters.sizes.length > 0 && !filters.sizes.some((size) => product.sizes.includes(size))) {
      return false
    }

    // Price range filter
    if (product.price < filters.priceRange[0] || product.price > filters.priceRange[1]) {
      return false
    }

    return true
  })
}

